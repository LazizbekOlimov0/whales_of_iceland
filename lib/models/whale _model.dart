import 'dart:ui';

class Whale {
  final int id;
  final String name;
  final String min;
  final String type;
  final String imageUrl;
  final String mts;
  final String tons;
  final String speed;
  final String about;

  Whale({
    required this.id,
    required this.name,
    required this.min,
    required this.type,
    required this.imageUrl,
    required this.mts,
    required this.tons,
    required this.speed,
    required this.about,
  });
}